/// Load a palette from a .hex file (one hex color per line, e.g., "FF0000").
pub fn load_hex_file(path: &std::path::Path) -> Result<Vec<[u8; 3]>, String> {
    let content = std::fs::read_to_string(path)
        .map_err(|e| format!("Failed to read palette file {}: {}", path.display(), e))?;
    parse_hex_palette(&content)
}

/// Parse hex palette text (one color per line, "#RRGGBB" or "RRGGBB").
pub fn parse_hex_palette(content: &str) -> Result<Vec<[u8; 3]>, String> {
    let mut colors = Vec::new();
    for (i, line) in content.lines().enumerate() {
        let line = line.trim();
        if line.is_empty() || line.starts_with(';') || line.starts_with("//") {
            continue;
        }
        let hex = line.trim_start_matches('#');
        if hex.len() != 6 {
            return Err(format!("Line {}: invalid hex color '{}'", i + 1, line));
        }
        let r = u8::from_str_radix(&hex[0..2], 16)
            .map_err(|e| format!("Line {}: {}", i + 1, e))?;
        let g = u8::from_str_radix(&hex[2..4], 16)
            .map_err(|e| format!("Line {}: {}", i + 1, e))?;
        let b = u8::from_str_radix(&hex[4..6], 16)
            .map_err(|e| format!("Line {}: {}", i + 1, e))?;
        colors.push([r, g, b]);
    }
    if colors.is_empty() {
        return Err("Palette file contains no colors".to_string());
    }
    Ok(colors)
}

/// All built-in palettes.
pub const ALL_PALETTES: &[&PredefinedPalette] = &[
    &PICO_8,
    &SWEETIE_16,
    &ENDESGA_32,
    &ENDESGA_64,
    &GAMEBOY,
    &NES,
];

/// Find a predefined palette by slug name (case-insensitive).
pub fn find_palette(slug: &str) -> Option<&'static PredefinedPalette> {
    let slug_lower = slug.to_lowercase();
    ALL_PALETTES
        .iter()
        .find(|p| p.slug.to_lowercase() == slug_lower)
        .copied()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_find_palette() {
        assert!(find_palette("pico-8").is_some());
        assert!(find_palette("PICO-8").is_some());
        assert!(find_palette("sweetie-16").is_some());
        assert!(find_palette("nonexistent").is_none());
    }

    #[test]
    fn test_pico8_has_16_colors() {
        assert_eq!(PICO_8.colors.len(), 16);
    }

    #[test]
    fn test_parse_hex_palette() {
        let content = "FF0000\n00FF00\n0000FF\n";
        let colors = parse_hex_palette(content).unwrap();
        assert_eq!(colors, vec![[255, 0, 0], [0, 255, 0], [0, 0, 255]]);
    }

    #[test]
    fn test_parse_hex_palette_with_hash() {
        let content = "#FF0000\n#00FF00\n";
        let colors = parse_hex_palette(content).unwrap();
        assert_eq!(colors, vec![[255, 0, 0], [0, 255, 0]]);
    }

    #[test]
    fn test_parse_hex_palette_with_comments() {
        let content = "; This is a comment\nFF0000\n// Also a comment\n00FF00\n\n";
        let colors = parse_hex_palette(content).unwrap();
        assert_eq!(colors, vec![[255, 0, 0], [0, 255, 0]]);
    }

    #[test]
    fn test_parse_hex_palette_empty() {
        assert!(parse_hex_palette("").is_err());
        assert!(parse_hex_palette("; only comments\n").is_err());
    }
}
